// Reads a Sudoku puzzle from standard input, treating zeroes as empty cells.

// Local
#include "jut/fun/sudoku.hpp"

// C++ standard
#include <iostream>

int main() {
    jut::fun::sudoku::board b;
    if (!(std::cin >> b)) {
        std::cerr << "error: bad input\n";
        return 1;
    }
    if (!solve(b)) {
        std::cerr << "error: board has not solution\n";
        return 2;
    }
    // TODO Warn if the board has multiple solutions.
    std::cout << b << '\n';
}
